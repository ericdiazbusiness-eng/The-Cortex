import { existsSync, readFileSync } from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import pg from 'pg'

const { Pool } = pg

const projectRoot = process.cwd()

const stripWrappingQuotes = (value) => {
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1)
  }

  return value
}

const loadEnvFile = (fileName) => {
  const envPath = path.resolve(projectRoot, fileName)
  if (!existsSync(envPath)) {
    return
  }

  for (const rawLine of readFileSync(envPath, 'utf8').split(/\r?\n/u)) {
    const line = rawLine.trim()
    if (!line || line.startsWith('#')) {
      continue
    }

    const separatorIndex = line.indexOf('=')
    if (separatorIndex < 1) {
      continue
    }

    const key = line.slice(0, separatorIndex).trim()
    const value = stripWrappingQuotes(line.slice(separatorIndex + 1).trim())
    if (key && !process.env[key]) {
      process.env[key] = value
    }
  }
}

loadEnvFile('.env.local')
loadEnvFile('.env')

const connectionString = process.env.SUPABASE_DATABASE_URL || process.env.DATABASE_URL
if (!connectionString) {
  throw new Error('SUPABASE_DATABASE_URL is not configured.')
}

const pool = new Pool({
  connectionString,
  ssl: {
    rejectUnauthorized: false,
  },
  max: 1,
})

try {
  const client = await pool.connect()
  try {
    const identity = await client.query(
      'select current_database() as database_name, current_user as user_name',
    )

    await client.query(`
      create table if not exists public.cortex_connection_checks (
        id bigint generated by default as identity primary key,
        checked_at timestamptz not null default now(),
        source text not null,
        metadata jsonb not null default '{}'::jsonb
      )
    `)

    const insert = await client.query(
      `
        insert into public.cortex_connection_checks (source, metadata)
        values ($1, $2::jsonb)
        returning id, checked_at
      `,
      [
        'supabase-smoke-test',
        JSON.stringify({
          project: 'The Cortex',
          checkedBy: 'scripts/supabase-smoke-test.mjs',
        }),
      ],
    )

    console.log(
      JSON.stringify(
        {
          ok: true,
          database: identity.rows[0].database_name,
          user: identity.rows[0].user_name,
          inserted: insert.rows[0],
        },
        null,
        2,
      ),
    )
  } finally {
    client.release()
  }
} finally {
  await pool.end()
}
